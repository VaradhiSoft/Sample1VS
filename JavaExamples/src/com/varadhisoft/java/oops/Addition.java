package com.varadhisoft.java.oops;

public class Addition {

	/**
	 * @param args
	 */
	public static void main(String[] args) {  // 2 3 
      int n1 = Integer.parseInt(args[0]);    //  "2"  
      int n2 = Integer.parseInt(args[1]);    // "3"
      
      System.out.println("Sum:"+n1+n2);   // "Sum:3"+6  ==> "Sum:36"
		
		
	}

}

//  >java Addition 3 6
